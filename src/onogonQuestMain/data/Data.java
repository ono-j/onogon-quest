package onogonQuestMain.data;

public class Data {
	int id;
	int characterNo;
	String name;
	int hp;
	int mp;
	int money;

	public Data() {
	}

	public int getId() {
		return this.id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCharacterNo() {
		return this.characterNo;
	}

	public void setCharacterNo(int characterNo) {
		this.characterNo = characterNo;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getHp() {
		return this.hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}

	public int getMp() {
		return this.mp;
	}

	public void setMp(int mp) {
		this.mp = mp;
	}

	public int getMoney() {
		return this.money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

}
